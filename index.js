"use strict";
const A = 10;
const B = 5;
if (A > B) {
    console.log(`${A} es mayor que ${B}`);
}
else {
    console.log(`${A} es menor que ${B}`);
}
let i = 0;
do {
    console.log(i + " elefantes");
    i += 1;
} while (i < A);
let j = 0;
while (j < 4) {
    console.log(j + " jirafas");
    j += 1;
}
const C = 5;
for (let k = 0; k < C; k++) {
    console.log(k + " tortugas");
}
